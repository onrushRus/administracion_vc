	public function clearSocios()
	{
		$this->collSocios = null; // important to set this to NULL since that means it is uninitialized
	}

	/**
	 * Initializes the collSocios collection.
	 *
	 * By default this just sets the collSocios collection to an empty array (like clearcollSocios());
	 * however, you may wish to override this method in your stub class to provide setting appropriate
	 * to your application -- for example, setting the initial array to the values stored in database.
	 *
	 * @param      boolean $overrideExisting If set to true, the method call initializes
	 *                                        the collection even if it is not empty
	 *
	 * @return     void
	 */
	public function initSocios($overrideExisting = true)
	{
		if (null !== $this->collSocios && !$overrideExisting) {
			return;
		}
		$this->collSocios = new PropelObjectCollection();
		$this->collSocios->setModel('Socio');
	}

	/**
	 * Gets an array of Socio objects which contain a foreign key that references this object.
	 *
	 * If the $criteria is not null, it is used to always fetch the results from the database.
	 * Otherwise the results are fetched from the database the first time, then cached.
	 * Next time the same method is called without $criteria, the cached collection is returned.
	 * If this TipoSocio is new, it will return
	 * an empty collection or the current collection; the criteria is ignored on a new object.
	 *
	 * @param      Criteria $criteria optional Criteria object to narrow the query
	 * @param      PropelPDO $con optional connection object
	 * @return     PropelCollection|array Socio[] List of Socio objects
	 * @throws     PropelException
	 */
	public function getSocios($criteria = null, PropelPDO $con = null)
	{
		if(null === $this->collSocios || null !== $criteria) {
			if ($this->isNew() && null === $this->collSocios) {
				// return empty collection
				$this->initSocios();
			} else {
				$collSocios = SocioQuery::create(null, $criteria)
					->filterByTipoSocio($this)
					->find($con);
				if (null !== $criteria) {
					return $collSocios;
				}
				$this->collSocios = $collSocios;
			}
		}
		return $this->collSocios;
	}

	/**
	 * Sets a collection of Socio objects related by a one-to-many relationship
	 * to the current object.
	 * It will also schedule objects for deletion based on a diff between old objects (aka persisted)
	 * and new objects from the given Propel collection.
	 *
	 * @param      PropelCollection $socios A Propel collection.
	 * @param      PropelPDO $con Optional connection object
	 */
	public function setSocios(PropelCollection $socios, PropelPDO $con = null)
	{
		$this->sociosScheduledForDeletion = $this->getSocios(new Criteria(), $con)->diff($socios);

		foreach ($socios as $socio) {
			// Fix issue with collection modified by reference
			if ($socio->isNew()) {
				$socio->setTipoSocio($this);
			}
			$this->addSocio($socio);
		}

		$this->collSocios = $socios;
	}

	/**
	 * Returns the number of related Socio objects.
	 *
	 * @param      Criteria $criteria
	 * @param      boolean $distinct
	 * @param      PropelPDO $con
	 * @return     int Count of related Socio objects.
	 * @throws     PropelException
	 */
	public function countSocios(Criteria $criteria = null, $distinct = false, PropelPDO $con = null)
	{
		if(null === $this->collSocios || null !== $criteria) {
			if ($this->isNew() && null === $this->collSocios) {
				return 0;
			} else {
				$query = SocioQuery::create(null, $criteria);
				if($distinct) {
					$query->distinct();
				}
				return $query
					->filterByTipoSocio($this)
					->count($con);
			}
		} else {
			return count($this->collSocios);
		}
	}

	/**
	 * Method called to associate a Socio object to this object
	 * through the Socio foreign key attribute.
	 *
	 * @param      Socio $l Socio
	 * @return     TipoSocio The current object (for fluent API support)
	 */
	public function addSocio(Socio $l)
	{
		if ($this->collSocios === null) {
			$this->initSocios();
		}
		if (!$this->collSocios->contains($l)) { // only add it if the **same** object is not already associated
			$this->doAddSocio($l);
		}

		return $this;
	}

	/**
	 * @param	Socio $socio The socio object to add.
	 */
	protected function doAddSocio($socio)
	{
		$this->collSocios[]= $socio;
		$socio->setTipoSocio($this);
	}

	/**
	 * Clears the current object and sets all attributes to their default values
